# ** 指数含义   2**3=8
print("**代表指数操作 2**3 is: ", 2**3) 

# %为取模操作, 22%8=6
print("%代表取模操作 22%8 is: ", 22%8)

# //为整除操作, 22//8=2
print("//为整除操作, 22//8 is: ", 22//8) 

# /为带余除法操作,22/8=2.75
print("/为带余除法操作,22/8 is: ", 22/8)

# *为乘法操作, 3*5=15
print("*为乘法操作, 3*5 is: ", 3*5)

# -为减法操作, 5-2=3
print("-为减法操作, 5-2 is: ", 5-2)

# 操作符的优先级和数学中类似,括号优先级最高,从左到右进行计算
print(2 + 3 * 6)
print((2+3) * 6)
print((5-1)* ((7+1) / (3-1)))
