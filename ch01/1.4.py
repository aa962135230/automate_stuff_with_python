# 使用变量:
# 用于存放一个值,类似于使用一个带标签的盒子盛放东西,通过标签可以找到盒子所装的东西.
# 赋值格式: 变量名 = 值
spam = 40
print("spam is:", spam)

eggs=2
print("spam + eggs is: ", spam+eggs)
print("spam + eggs + spam is: ", spam+eggs+spam)

print("进行spam = spam + 2操作后")
spam = spam + 2
print("spam is: ",spam)

